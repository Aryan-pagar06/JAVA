public class escape
{
    public static void main(String[] args) 
    {
        System.out.println("hello \"aryan pagar\" from mit wpu");  //to display double quotes
        System.out.println("hello\naryan"); // to leave a line
        System.out.println("aryan \t pagar"); // for tab
        System.out.println("hello \\aryan pagar\\ from mit wpu");  //to display back slash
        System.out.println("hello\baryan pagar from mit wpu");  //to get before
    }
}